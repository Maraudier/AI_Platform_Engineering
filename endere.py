def endere(a: int) -> str:
    """ This function will return a different sentence depending on whether a is 1 or 2. 
    
    Parameter - 
    a: int
    
    Return - 
    string
    
    """
    if (a == 1):
        return ("This is my first string.")
    else: 
        return ("This is my second string.")
    
